#include "shell.h"

int		sh_squote(char *str, int *t)
{
	int b;
	int i;

	b = 0;
	i = *t;
	while (str[i])
	{
		if (str[i] == '\\' && str[i + 1])
			i++;
		else if (str[i] == '\'')
			b++;
		if ((!str[i + 1] && b == 1) || (str[i] == '`' && b == 2))
		{
			b = 0;
			*t = i;
		}
		i++;
	}
	return (0);
}
